package package23;

public class alphabet 
{
	public static void alphabetidentify(String alpha)
	{
		switch(alpha)
		{
		case"a","e","i","o","u":
			System.out.println("Vowel");
		break;
		case"A","E","I","O","U":
			System.out.println("Vowel");
		break;
		default:
			System.out.println("Consonant");
		break;
			
		}
	}

}
